package Replit;

public class ForWhile10 {
    /*1'den 100'e kadar olan doğal sayıların toplamını bulan programı yazınız.

OutPut:

Sayilarin Toplami : 5050

*/
    public static void main(String[] args) {
        int toplam=0;
        int sayi=1;
        while (sayi>0){
            System.out.println("Sayilar toplami: "+toplam);
            toplam++;
        }
    }
}
